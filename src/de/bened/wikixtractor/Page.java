package de.bened.wikixtractor;

import java.util.Set;

/**
 * Created by xuiqzy.
 */
class Page {
    private final int pageID;
    private final int namespaceID;
    private final String title;
    private final Set<String> categories;

    Page(int pageID, int namespaceID, String title, Set<String> categories) {
        this.pageID = pageID;
        this.namespaceID = namespaceID;
        this.title = title;
        this.categories = categories;
    }


    int getPageID() {
        return pageID;
    }

    int getNamespaceID() {
        return namespaceID;
    }

    String getTitle() {
        return title;
    }

    Set<String> getCategories() {
        return categories;
    }


    // generated by Intellij Idea, simplified by me (xuiqzy) with the inspection hints from Intellij Idea
    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Page page = (Page) o;

        return pageID == page.pageID && namespaceID == page.namespaceID &&
                (title != null ? title.equals(page.title) : page.title == null) &&
                (categories != null ? categories.equals(page.categories) : page.categories == null);
    }

    // generated by Intellij Idea
    @Override
    public int hashCode() {
        int result = pageID;
        result = 31 * result + namespaceID;
        result = 31 * result + (title != null ? title.hashCode() : 0);
        result = 31 * result + (categories != null ? categories.hashCode() : 0);
        return result;
    }
}
